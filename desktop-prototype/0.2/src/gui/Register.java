    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(!isEmpty(jTextField1.getText())&&!isEmpty(jTextField2.getText())&&!isEmpty(jTextField3.getText())&&!isEmpty(jTextField4.getText())&&!isEmpty(jTextField5.getText())){
            
            username = jTextField1.getText();
            email = jTextField2.getText();
            password = jTextField3.getText();
            age = Integer.parseInt(jTextField4.getText());
            gender = jTextField5.getText();

            Account a = new Account();
            a.age = age;
            a.email = email;
            a.gender = gender;
            a.password = password;
            a.type = type;
            a.name = username;
            
            Database db = new Database(a);
            
            boolean result = false;
            
            try {
                result = db.register();
            } catch (IOException ex) {
                Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(result == false){
                 jLabel7.setText("ReEnterFields");
            }else{
                jLabel7.setText("Successfully Registerd");
            }
            try {
                db.login();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            jLabel7.setText("Enetr Required Fields");
        }
        
        // open interactive
        
        this.dispose();
        
    }//GEN-LAST:event_jButton1ActionPerformed

