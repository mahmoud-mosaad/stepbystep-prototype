    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(!isEmpty(jTextField3.getText())&&!isEmpty(jTextField1.getText())){
            Account a = new Account();
            a.password = jTextField3.getText();
            a.name = jTextField1.getText();
            Database db = new Database(a);
            
            try {
                boolean login = db.login();
                
                if(login){
                    if(db.getType().equals("S")){
                        InteractS ins = new InteractS();
                        ins.setVars(jTextField1.getText(), jTextField3.getText());
                        this.dispose();
                    }   else{
                        InteractT ins = new InteractT();
                        ins.setVars(jTextField1.getText(), jTextField3.getText());
                        this.dispose();
                    }  
                }else{
                     jLabel2.setText("Enter correct fields");
                 }
                
                
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
            
           
            
        }else{
            jLabel2.setText("Enter all required fields");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

